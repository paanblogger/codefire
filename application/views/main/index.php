<h2>Sistem 1 Jururawat 1 Pesakit</h2>
<p>Ketua Pengarah Kesihatan Malaysia telah mengarahkan semua klinik kesihatan / klinik desa melaksanakan Perkhidmatan "Personalised Care".</p>

<strong>Konsep "Personalised Care"</strong>
<ul>
	<li>Pemberian penjagaan kesihatan seorang individu dilaksanakan oleh seorang anggota kesihatan diklinik kesihatan / klinik desa dalam tempoh mereka mendapatkan perkhidmtan kesihatan . Sebagai contoh dari ibu mengandung sehingga posnatal dan seterusnya.</li>
</ul>
<strong>Objektif Umum</strong>
<ul>
	<li>Penjagaan seorang individu / klien kepada seorang anggota kesihatan di Klinik Kesihatan Dan Klinik Desa</li>
</ul>
<strong>Objektif Khusus</strong>
<ul>
	<li>Memberi jagaan kepada klien secara individu yang mana rawatan dan penjagaan di klinik kesihatan / klinik desa berkesinambungan dari mula mendaftar sehingga diberi temujanji dan seterusnya lawatan susulan kerumah oleh seorang anggota kesihatan yang sama untuk mencapai kesihatan yang optima.</li>
</ul>